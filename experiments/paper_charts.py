        x=alt.X("prompt_label:N", sort=PROMPT_LABEL_ORDER),
        y="avg_v2_score_fee_filtered:Q",
        tooltip=["prompt_label", alt.Tooltip("avg_v2_score_fee_filtered:Q", format=".1f")],
